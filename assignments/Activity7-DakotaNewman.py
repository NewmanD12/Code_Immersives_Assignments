def add(num1, num2):
  sum = num1 + num2
  print(sum)
  return sum

def subtract(num1, num2):
  difference = num1 - num2
  print(difference)
  return difference

def multiply(num1, num2):
  total = num1 * num2
  print(total)
  return total

def divide(num1, num2):
  result = num1 / num2
  print(result)
  return result

add(5, 10)
subtract(100, 10)
multiply(7, 5)
divide(100, 10)